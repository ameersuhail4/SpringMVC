package com.todo;


import javax.validation.constraints.NotNull;
import javax.validation.constraints.Size;

public class Todo {
    private int id;
    private String user;

    @NotNull
    @Size(min =3,max = 10,message = "should be greater than 3 chars")
    private String desc;
    private int targetDate;
    private boolean isDone;

    public Todo(int id, String user, String desc, int targetDate, boolean isDone) {
        this.id = id;
        this.user = user;
        this.desc = desc;
        this.targetDate = targetDate;
        this.isDone = isDone;
    }

    public Todo() {

    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getUser() {
        return user;
    }

    public void setUser(String user) {
        this.user = user;
    }

    public String getDesc() {
        return desc;
    }

    public void setDesc(String desc) {
        this.desc = desc;
    }

    public int getTargetDate() {
        return targetDate;
    }

    public void setTargetDate(int targetDate) {
        this.targetDate = targetDate;
    }

    public boolean isDone() {
        return isDone;
    }

    public void setDone(boolean done) {
        isDone = done;
    }

    @Override
    public String toString() {
        return "Todo{" +
                "id=" + id +
                ", user='" + user + '\'' +
                ", desc='" + desc + '\'' +
                ", targetDate=" + targetDate +
                ", isDone=" + isDone +
                '}';
    }
}
